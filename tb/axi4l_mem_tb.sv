`include "axi/typedef.svh"

module axi4l_mem_tb;

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // Parameters
  //////////////////////////////////////////////////////////////////////////////////////////////////

  localparam int AW = 64;
  localparam int DW = 64;

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // Type definations
  //////////////////////////////////////////////////////////////////////////////////////////////////

  `AXI_LITE_TYPEDEF_ALL(axil, logic[AW-1:0], logic[DW-1:0], logic[DW/8-1:0])

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // Signals
  //////////////////////////////////////////////////////////////////////////////////////////////////

  logic arst_n;
  logic clk;

  axil_req_t req;
  axil_resp_t resp;

  bit is_clk_aligned;
  always @(posedge clk) begin
    is_clk_aligned = '1;
    #1fs;
    is_clk_aligned = '0;
  end

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // DUT
  //////////////////////////////////////////////////////////////////////////////////////////////////

  // axi4l_mem #(
  //     .axi4l_req_t (axil_req_t),
  //     .axi4l_resp_t(axil_resp_t),
  //     .ADDR_WIDTH  (AW),
  //     .DATA_WIDTH  (DW)
  // ) u_dut (
  //     .arst_ni     (arst_n),
  //     .clk_i       (clk),
  //     .axi4l_req_i (req),
  //     .axi4l_resp_o(resp)
  // );

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // Methods
  //////////////////////////////////////////////////////////////////////////////////////////////////

  task automatic apply_reset(realtime duration = 100ns);
    #(duration);
    arst_n <= '0;
    clk    <= '0;
    req    <= '0;
    #(duration);
    arst_n <= '1;
    #(duration);
  endtask

  task automatic start_clock(realtime tp = 10ns);
    fork
      forever #(tp / 2) clk <= ~clk;
    join_none
  endtask

  task automatic send_aw(input logic [AW-1:0] addr = '0, input logic [2:0] prot = '0);
    wait (is_clk_aligned);
    req.aw.addr  <= addr;
    req.aw.prot  <= prot;
    req.aw_valid <= '1;
    do @(posedge clk); while (resp.aw_ready !== '1);
    req.aw_valid <= '0;
  endtask

  task automatic recv_aw(output logic [AW-1:0] addr, output logic [2:0] prot);
    wait (is_clk_aligned);
    resp.aw_ready <= '1;
    do @(posedge clk); while (req.aw_valid !== '1);
    addr = req.aw.addr;
    prot = req.aw.prot;
    resp.aw_ready <= '0;
  endtask

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // ANNIM
  //////////////////////////////////////////////////////////////////////////////////////////////////

  // SEND W
  // RECV W

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // KHALID
  //////////////////////////////////////////////////////////////////////////////////////////////////

  task automatic send_b(input logic [1:0] bresp = 2'b00);
    wait (is_clk_aligned);
    resp.b.resp  <= bresp;
    resp.b_valid <= '1;
    do @(posedge clk); while (req.b_ready !== '1);
    resp.b_valid <= '0;
  endtask

  task automatic recv_b(output logic [1:0] bresp);
    wait (is_clk_aligned);
    req.b_ready <= '1;
    do @(posedge clk); while (resp.b_valid !== '1);
    bresp = resp.b.resp;
    req.b_ready <= '0;
  endtask

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // SABBIR
  //////////////////////////////////////////////////////////////////////////////////////////////////

  // SEND AR
  // RECV AR

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // SHAWON
  //////////////////////////////////////////////////////////////////////////////////////////////////

  // SEND R
  // RECV R

  //////////////////////////////////////////////////////////////////////////////////////////////////
  // Procedurals
  //////////////////////////////////////////////////////////////////////////////////////////////////

  initial begin
    $dumpfile("axi4l_mem_tb.vcd");
    $dumpvars(0, axi4l_mem_tb);

    apply_reset();
    start_clock();

    fork
      send_aw(64'hDEADBEEFCAFEBABE, 3'b101);
      #30ns recv_aw(req.aw.addr, req.aw.prot);
    join

    #1us;
    $finish;
  end

endmodule
